package com.jinganweigu.entrysystem.entry;

public class VersionBean {


    /**
     * code : 200
     * msg : 操作成功，请求成功!
     * result : 1
     */

    private int code;
    private String msg;
    private int result;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getResult() {
        return result;
    }

    public void setResult(int result) {
        this.result = result;
    }
}
